#define _CRT_SECURE_NO_WARNINGS 1
#include"contact.h"
void menu()
{
	printf("***********************************************\n");
	printf("********1.add*******************2.del**********\n");
	printf("********3.search****************4.modify*******\n");
	printf("********5.show******************6.sort*********\n");
	printf("******************0.exit***********************\n");
	printf("***********************************************\n");
}
int main()
{
		int input=0;
		struct Contact con;//con就是通讯录 里边包含一千个元素的数组和size
		InitContact(&con);
	do
	{
		
		//初始化通讯录
		menu();
		printf("请选择\n");
		scanf("%d",&input);
		switch(input)
		{
		case ADD:
			AddContact(&con);
			break;
		case DEL:
			Delcontact(&con);
			break;
		case SEARCH:
			SearchContact(&con);
			break;
		case MODIFY:
			ModIFYcontact(&con);
			break;
		case SHOW:
			ShowContact(&con);
			break;
		case SORT:
			break;
		case EXIT:
			printf("退出通讯录\n");
		default:
			printf("选择错误\n");
			break;
		}
		}
		while(input);
	return 0;
}